document.getElementById('cpfInput').addEventListener('input', function (e) {
    let value = e.target.value.replace(/\D/g, '');
    if (value.length > 11) value = value.slice(0, 11);

    value = value.replace(/(\d{3})(\d)/, '$1.$2');
    value = value.replace(/(\d{3})(\d)/, '$1.$2');
    value = value.replace(/(\d{3})(\d{1,2})$/, '$1-$2');

    e.target.value = value;
    validateCPF(); // Chama a validação a cada digitação
});

function validateCPF() {
    const rawCpf = document.getElementById('cpfInput').value;
    const cpf = rawCpf.replace(/\D/g, '');
    const feedback = document.getElementById('cpfFeedback');
    const consultaBtn = document.getElementById('consultaButton');
    const consultaLink = document.getElementById('consultaLink');

    // Verificações básicas
    if (cpf.length !== 11 || /^(\d)\1+$/.test(cpf)) {
        feedback.textContent = 'CPF inválido.';
        feedback.style.color = 'red';
        consultaBtn.disabled = true;
        consultaLink.href = '/confirmacao?cpf=';
        return false;
    }

    // Validação dos dígitos
    let sum = 0;
    for (let i = 0; i < 9; i++) sum += parseInt(cpf.charAt(i)) * (10 - i);
    let check1 = 11 - (sum % 11);
    check1 = check1 >= 10 ? 0 : check1;

    sum = 0;
    for (let i = 0; i < 10; i++) sum += parseInt(cpf.charAt(i)) * (11 - i);
    let check2 = 11 - (sum % 11);
    check2 = check2 >= 10 ? 0 : check2;

    if (check1 === parseInt(cpf.charAt(9)) && check2 === parseInt(cpf.charAt(10))) {
        feedback.textContent = 'CPF válido.';
        feedback.style.color = 'green';
        consultaBtn.disabled = false;
        consultaLink.href = '/confirmacao?cpf=' + cpf;
        return true;
    } else {
        feedback.textContent = 'CPF inválido.';
        feedback.style.color = 'red';
        consultaBtn.disabled = true;
        consultaLink.href = '/confirmacao?cpf=';
        return false;
    }
}
